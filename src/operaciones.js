export function suma(string1, string2){
    return parseInt(string1)+parseInt(string2);
}

export function resta(string1, string2){
    return parseInt(string1)-parseInt(string2);
}

export function mult(string1, string2){
    return parseInt(string1)*parseInt(string2);
}

export function div(string1, string2){
    return parseInt(parseInt(string1)/parseInt(string2));
}

